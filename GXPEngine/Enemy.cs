using System;									// System contains a lot of default C# libraries 
using GXPEngine;								// GXPEngine contains the engine

namespace GXPEngine
{
    public class Enemy : AnimationSprite
    {
        int animationDrawsBetweenFrames;
        int step;

        int speed;

        bool goToLeft;
        bool goToRight;

        public Enemy() : base("enemy_tile.png", 6, 1)
        {
            Spawn();

            animationDrawsBetweenFrames = 5;
            speed = 3;
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Spawn()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Spawn()
        {
            SetFrame(1);

            SetXY(game.width / 2, game.height / 2);
            SetOrigin(width / 2, height / 2 + 100);

            goToRight = true;
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleAnimation()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleAnimation()
        {
            step = step + 1;

            if (step > animationDrawsBetweenFrames)
            {
                NextFrame();
                step = 0;
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleMovement()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleMovement()
        {
            if (x >= 1400)
            {
                goToRight = false;
                goToLeft = true;
            }

            if (x <= 40)
            {
                goToLeft = false;
                goToRight = true;
            }

            if (goToLeft == true)
            {
                x = x - speed;
            }

            if (goToRight == true)
            {
                x = x + speed;
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            HandleAnimation();
            HandleMovement();
        }
    }
}

