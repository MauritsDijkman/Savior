using System;									// System contains a lot of default C# libraries 
using GXPEngine;								// GXPEngine contains the engine

namespace GXPEngine
{
    class CreditsMenu : GameObject
    {
        Sprite creditsmenu;

        Button_Back _back_button;

        Sprite back_button_normal;
        Sprite back_button_hover;

        public CreditsMenu() : base()
        {
            creditsmenu = new Sprite("ControlsMenu.png");
            AddChild(creditsmenu);

            _back_button = new Button_Back();
            AddChild(_back_button);

            back_button_normal = new Sprite("back_main_button_normal.png");
            AddChild(back_button_normal);

            back_button_hover = new Sprite("back_main_button_hover.png");
            AddChild(back_button_hover);
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleClickButton()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleClickButton()
        {
            if (Input.GetMouseButtonUp(0))
            {
                if (_back_button.HitTestPoint(Input.mouseX, Input.mouseY))
                {
                    GoBackToStartMenu();
                }
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleHoverButton()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleHoverButton()
        {
            if (_back_button.HitTestPoint(Input.mouseX, Input.mouseY))
            {
                back_button_normal.visible = false;
                back_button_hover.visible = true;
            }
            else
            {
                back_button_normal.visible = true;
                back_button_hover.visible = false;
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        GoBackToStartMenu()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void GoBackToStartMenu()
        {
            DestroyCreditsMenu();

            MyGame mygame = game as MyGame;
            mygame.CreateMenu();

        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        DestroyCreditsMenu()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void DestroyCreditsMenu()
        {
            LateDestroy();
            LateRemove();
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            HandleClickButton();
            HandleHoverButton();
        }
    }
}
