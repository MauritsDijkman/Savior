using System;									// System contains a lot of default C# libraries 
using GXPEngine;								// GXPEngine contains the engine

namespace GXPEngine
{
    class Healthbar_Player : AnimationSprite
    {
        Sound _deathSound;

        public Healthbar_Player() : base("healthbar_player_tile.png", 6, 1)
        {
            _deathSound = new Sound("death_sound_player.wav", false, false);
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleGameOver()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleGameOver()
        {
            if (Globals.health_player == 0 && Globals.playerIsDead == false)
            {
                Globals.playerIsDead = true;

                MyGame mygame = game as MyGame;
                mygame.GameOver();

                _deathSound.Play();
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            SetFrame(Globals.health_player);
            HandleGameOver();
        }
    }
}
