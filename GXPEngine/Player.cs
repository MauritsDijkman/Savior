using System;									// System contains a lot of default C# libraries 
using GXPEngine;								// GXPEngine contains the engine

namespace GXPEngine
{
    public class Player : AnimationSprite
    {
        int animationDrawsBetweenFrames;
        int step;

        int speed;

        bool playerIsMoving;

        public Player() : base("player_tile.png", 6, 1)
        {
            Spawn();

            animationDrawsBetweenFrames = 5;
            speed = 5;
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Spawn()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Spawn()
        {
            SetFrame(1);

            SetXY(game.width / 2, game.height / 2);
            SetOrigin(width / 2, height / 2);
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleAnimation()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleAnimation()
        {
            if (Input.GetKeyDown(Key.W) || Input.GetKeyDown(Key.S) || Input.GetKeyDown(Key.D) || Input.GetKeyDown(Key.A))
            {
                playerIsMoving = true;
            }

            if (Input.GetKeyUp(Key.W) || Input.GetKeyUp(Key.S) || Input.GetKeyUp(Key.D) || Input.GetKeyUp(Key.A))
            {
                playerIsMoving = false;
            }

            if (playerIsMoving == true)
            {
                step = step + 1;

                if (step > animationDrawsBetweenFrames)
                {
                    NextFrame();
                    step = 0;
                }
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleMovement()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleMovement()
        {
            if (Input.GetKey(Key.W))
            {
                Move(0, -speed);
            }

            if (Input.GetKey(Key.S))
            {
                Move(0, speed);
            }

            if (Input.GetKey(Key.D))
            {
                Move(speed, 0);
            }

            if (Input.GetKey(Key.A))
            {
                Move(-speed, 0);
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        HandleBorders()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void HandleBorders()
        {
            x = Mathf.Clamp(x, (0 + (width / 6) + 12), (1440 - (width / 6)));
            y = Mathf.Clamp(y, (0 + (height / 2) - 18), (1080 - (height / 2)));
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            HandleAnimation();
            HandleMovement();
            HandleBorders();
        }
    }
}
