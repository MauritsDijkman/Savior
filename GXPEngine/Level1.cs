using System;									// System contains a lot of default C# libraries 
using GXPEngine;								// GXPEngine contains the engine

namespace GXPEngine
{
    public class Level1 : GameObject
    {
        Player player;
        Level1_Background background;
        Block_Jump _block_jump;

        Enemy enemy1;
        Enemy enemy2;
        Enemy enemy3;
        Enemy enemy4;
        Enemy enemy5;

        public Level1() : base()
        {
            StartLevel();
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        StartLevel()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void StartLevel()
        {
            background = new Level1_Background();
            AddChild(background);

            player = new Player();
            AddChild(player);

            enemy1 = new Enemy(1220, 700, 1440, 2880);
            AddChild(enemy1);

            _block_jump = new Block_Jump();
            AddChild(_block_jump);

            _block_jump.x = 5040;
            _block_jump.y = 600;
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            if (player.x + x > 800) x = 800 - player.x;
            if (player.x + x < 500) x = 500 - player.x;
        }
    }
}
