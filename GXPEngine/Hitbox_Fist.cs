using System;
using GXPEngine;

namespace GXPEngine
{
    class Hitbox_Fist : Sprite
    {
        Sound _damageSound;

        public Hitbox_Fist() : base("hitbox_fist.png")
        {
            _damageSound = new Sound("damage_sound_player.wav", false, false);

            //visible = false;
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        FistUpdate()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        public void FistUpdate()
        {
            if (Globals.MCfacingLeft == false && Globals.MCfacingRight == true)
            {
                SetOrigin(-50, 180);
                SetXY(0, 0);
            }

            if (Globals.MCfacingLeft == true && Globals.MCfacingRight == false)
            {
                SetOrigin(90, 180);
                SetXY(0, 0);
            }
        }

        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        //                                                                                                                        Update()
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
        void Update()
        {
            FistUpdate();
        }
    }
}
